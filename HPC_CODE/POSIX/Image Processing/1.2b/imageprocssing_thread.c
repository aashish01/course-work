#define width 100 
#define height 72

unsigned char image[], results[width * height];

typedef struct arguments {
unsigned char *input;
unsigned char *output;
  int start;
  int stride;
} 
arguments_t;

void edges(unsigned char *image,unsigned char *results) {
  pthread_t a1, a2, a3, a4;

  arguments_t a1_arguments;
  a1_arguments.start = 0;
  a1_arguments.stride = 4;
  a1_arguments.input = image;
  a1_arguments.output = results;


  arguments_t a2_arguments;
  a2_arguments.start = 1;
  a2_arguments.stride = 4;
  a2_arguments.input = image;
  a2_arguments.output = results;

  arguments_t a3_arguments;
  a3_arguments.start = 2;
  a3_arguments.stride = 4;
  a3_arguments.input = image;
  a3_arguments.output = results;

  arguments_t a4_arguments;
  a4_arguments.start = 3;
  a4_arguments.stride = 4;
  a4_arguments.input = image;
  a4_arguments.output = results;

  void *detect_edges();
  
  pthread_create(&a1, NULL, detect_edges, &a1_arguments);
  pthread_create(&a2, NULL, detect_edges, &a2_arguments);
  pthread_create(&a3, NULL, detect_edges, &a3_arguments);
  pthread_create(&a4, NULL, detect_edges, &a4_arguments);

  pthread_join(a1, NULL);
  pthread_join(a2, NULL);
  pthread_join(a3, NULL);
  pthread_join(a4, NULL);
}



void *detect_edges(arguments_t *args) {
  int i;
  unsigned char *in = args->input;
  unsigned char *out =args->output;
  int n_pixels = width * height;

  for(i=args->start;i<n_pixels;i+=args->stride) {
    int p, q; // the pixel of interest
    int b, d, f, h; // the pixels adjacent to p,q used for the calculation
    int r; // the result of calculate
    
    q = i / width;
    p = i - (width * q);

    if (p == 0 || q == 0 || p == width - 1 || q == height - 1) {
      results[i] = 0;
    } else {
      b = i + width;
      d = i - 1;
      f = i + 1;
      h = i - width;

      r = (in[i] * 4) + (in[b] * -1) + (in[d] * -1) + (in[f] * -1)
          + (in[h] * -1);

      if (r > 0) { // if the result is positive this is an edge pixel
        out[i] = 255;
      } else {
        out[i] = 0;
      }
    }
  }
}

int time_difference(struct timespec *start, struct timespec *finish, 
                    long long int *difference) {
  long long int ds =  finish->tv_sec - start->tv_sec; 
  long long int dn =  finish->tv_nsec - start->tv_nsec; 

  if(dn < 0 ) {
    ds--;
    dn += 1000000000; 
  } 
  *difference = ds * 1000000000 + dn;
  return !(*difference > 0);
}

void tidy_and_exit() {
  exit(0);
}

void sigint_callback(int signal_number){
  printf("\nInterrupt from keyboard\n");
  tidy_and_exit();
}

static void display() {
  glClear(GL_COLOR_BUFFER_BIT);
  glRasterPos4i(-1, -1, 0, 1);
  glDrawPixels(width, height, GL_LUMINANCE, GL_UNSIGNED_BYTE, image);
  glRasterPos4i(0, -1, 0, 1);
  glDrawPixels(width, height, GL_LUMINANCE, GL_UNSIGNED_BYTE, results);
  glFlush();
}

static void key_pressed(unsigned char key, int p, int q) {
  switch(key){
    case 27: // escape
      tidy_and_exit();
      break;
    default: 
      printf("\nPress escape to exit\n");
      break;
  }
}

int main(int argc, char **argv) {
  signal(SIGINT, sigint_callback);
 
  printf("image dimensions %dp%d\n", width, height);

 struct timespec start, finish;   
  long long int time_elapsed;

  clock_gettime(CLOCK_MONOTONIC, &start);
 
  edges(image, results);

clock_gettime(CLOCK_MONOTONIC, &finish);
  time_difference(&start, &finish, &time_elapsed);
  printf("Time elapsed was %lldns or %0.9lfs\n", time_elapsed, 
         (time_elapsed/1.0e9)); 

  glutInit(&argc, argv);
  glutInitWindowSize(width * 2,height);
  glutInitDisplayMode(GLUT_SINGLE | GLUT_LUMINANCE);
      
  glutCreateWindow("6CS005 Image Progessing Courework");
  glutDisplayFunc(display);
  glutKeyboardFunc(key_pressed);
  glClearColor(0.0, 1.0, 0.0, 1.0); 

  glutMainLoop(); 

  tidy_and_exit();
  
  return 0;
}

