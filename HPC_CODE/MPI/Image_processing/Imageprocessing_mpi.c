#define width 100
#define height 72

unsigned char image[], results[width * height];
int starting_index, ending_index;

void detect_edges(unsigned char *in, unsigned char *out) {
  int i;
  int n_pixels = (width * height);

  for(i=0;i<n_pixels;i++) {
    int t, h;
    int a, s, d, k;
    int r;
   
    h = i / width;
    t = i - (width * h);

    if (t == 0 || h == 0 || t == width - 1 || h == height - 1) {
      results[i] = 0;
    } else {
      a = i + width;
      s = i - 1;
      d = i + 1;
      k = i - width;

      r = (in[i] * 4) + (in[a] * -1) + (in[s] * -1) + (in[d] * -1)
          + (in[k] * -1);

      if (r > 0) {
        out[i] = 255;
      } else {
        out[i] = 0;
      }
    }
  }
}

void tidy_and_exit() {
  exit(0);
}

void sigint_callback(int signal_number){
  printf("\nInterrupt from keyboard\n");
  tidy_and_exit();
}

static void display() {
  glClear(GL_COLOR_BUFFER_BIT);
  glRasterPos4i(-1, -1, 0, 1);
  glDrawPixels(width, height, GL_LUMINANCE, GL_UNSIGNED_BYTE, image);
  glRasterPos4i(0, -1, 0, 1);
  glDrawPixels(width, height, GL_LUMINANCE, GL_UNSIGNED_BYTE, results);
  glFlush();
}

static void key_pressed(unsigned char key, int t, int h) {
  switch(key){
    case 27:
      tidy_and_exit();
      break;
    default:
      printf("\nPress escape to exit\n");
      break;
  }
}

// Used for calculating Time
int time_difference(struct timespec *start, struct timespec *finish,
                              long long int *difference) {
  long long int ds =  finish->tv_sec - start->tv_sec;
  long long int dn =  finish->tv_nsec - start->tv_nsec;

  if(dn < 0 ) {
    ds--;
    dn += 1000000000;
  }
  *difference = ds * 1000000000 + dn;
  return !(*difference > 0);
}

